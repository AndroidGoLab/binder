package main

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/xaionaro-go/binder/tools/pkg/parcelspec"
	"gopkg.in/yaml.v3"
)

func main() {
	specs := flag.String("specs", "parcelspecs", "Directory containing YAML spec files")
	output := flag.String("output", ".", "Output directory (project root)")
	flag.Parse()

	if err := run(*specs, *output); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %v\n", err)
		os.Exit(1)
	}
}

func run(
	specsDir string,
	outputDir string,
) error {
	generated := 0
	skipped := 0

	err := filepath.Walk(specsDir, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}

		if info.IsDir() || !strings.HasSuffix(path, ".yaml") {
			return nil
		}

		data, err := os.ReadFile(path)
		if err != nil {
			return fmt.Errorf("reading %s: %w", path, err)
		}

		var spec parcelspec.ParcelableSpec
		if err := yaml.Unmarshal(data, &spec); err != nil {
			return fmt.Errorf("parsing %s: %w", path, err)
		}

		if len(spec.Fields) == 0 {
			skipped++
			return nil
		}

		pkgPath := strings.ReplaceAll(spec.Package, ".", "/")
		targetDir := filepath.Join(outputDir, pkgPath)

		// Only write if the output directory exists (package was generated by aospgen).
		dirInfo, err := os.Stat(targetDir)
		if err != nil || !dirInfo.IsDir() {
			skipped++
			return nil
		}

		goSrc, err := parcelspec.GenerateGo(spec)
		if err != nil {
			return fmt.Errorf("generating Go for %s.%s: %w", spec.Package, spec.Type, err)
		}

		filename := strings.ToLower(spec.Type) + ".go"
		outPath := filepath.Join(targetDir, filename)
		if err := os.WriteFile(outPath, goSrc, 0o644); err != nil {
			return fmt.Errorf("writing %s: %w", outPath, err)
		}

		generated++
		return nil
	})
	if err != nil {
		return fmt.Errorf("walking %s: %w", specsDir, err)
	}

	fmt.Fprintf(os.Stderr, "genparcelgo: generated %d files, skipped %d specs\n", generated, skipped)
	return nil
}
