package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/xaionaro-go/binder/tools/pkg/codegen"
	"github.com/xaionaro-go/binder/tools/pkg/servicemap"
)

func main() {
	frameworksBase := flag.String("frameworks-base", "tools/pkg/3rdparty/frameworks-base", "Path to the AOSP frameworks-base directory")
	output := flag.String("output", "", "Output file path for JSON (default: stdout)")
	goConstants := flag.String("go-constants", "", "Output file path for generated Go constants")
	flag.Parse()

	if err := run(*frameworksBase, *output, *goConstants); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %v\n", err)
		os.Exit(1)
	}
}

func run(
	frameworksBase string,
	output string,
	goConstants string,
) error {
	if goConstants != "" {
		if err := generateGoConstants(frameworksBase, goConstants); err != nil {
			return fmt.Errorf("generating Go constants: %w", err)
		}
	}

	svcMap, err := servicemap.BuildServiceMap(frameworksBase)
	if err != nil {
		return fmt.Errorf("building service map: %w", err)
	}

	// Build a sorted ordered map for deterministic JSON output.
	keys := make([]string, 0, len(svcMap))
	for k := range svcMap {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	ordered := make(orderedMap, 0, len(keys))
	for _, k := range keys {
		ordered = append(ordered, mapEntry{
			Key:   k,
			Value: svcMap[k],
		})
	}

	data, err := json.MarshalIndent(ordered, "", "  ")
	if err != nil {
		return fmt.Errorf("marshaling JSON: %w", err)
	}
	data = append(data, '\n')

	if output == "" {
		_, err = os.Stdout.Write(data)
		return err
	}

	return os.WriteFile(output, data, 0o644)
}

// generateGoConstants reads Context.java, extracts all *_SERVICE constants,
// and writes a Go source file with typed ServiceName constants.
func generateGoConstants(
	frameworksBase string,
	outputPath string,
) error {
	contextPath := filepath.Join(frameworksBase, "core/java/android/content/Context.java")
	src, err := os.ReadFile(contextPath)
	if err != nil {
		return fmt.Errorf("reading Context.java: %w", err)
	}

	constants := servicemap.ExtractContextConstants(string(src))

	type constEntry struct {
		GoName string
		Value  string
	}

	entries := make([]constEntry, 0, len(constants))
	for javaName, value := range constants {
		goName := codegen.ScreamingSnakeToPascal(javaName)
		entries = append(entries, constEntry{
			GoName: goName,
			Value:  value,
		})
	}

	sort.Slice(entries, func(i, j int) bool {
		return entries[i].GoName < entries[j].GoName
	})

	var buf strings.Builder
	buf.WriteString("// Code generated by genservicemap. DO NOT EDIT.\n\n")
	buf.WriteString("package servicemanager\n\n")
	buf.WriteString("// Service name constants extracted from android.content.Context.\n")
	buf.WriteString("const (\n")
	for _, e := range entries {
		fmt.Fprintf(&buf, "\t%s ServiceName = %q\n", e.GoName, e.Value)
	}
	buf.WriteString(")\n")

	formatted, err := format.Source([]byte(buf.String()))
	if err != nil {
		return fmt.Errorf("formatting generated Go source: %w", err)
	}

	return os.WriteFile(outputPath, formatted, 0o644)
}

// mapEntry is one key-value pair in our ordered JSON object.
type mapEntry struct {
	Key   string
	Value servicemap.ServiceMapEntry
}

// orderedMap preserves insertion order when marshaled to JSON,
// producing an object with keys sorted by service name.
type orderedMap []mapEntry

func (o orderedMap) MarshalJSON() ([]byte, error) {
	// Build the JSON object manually to preserve key order.
	buf := []byte{'{'}
	for i, entry := range o {
		if i > 0 {
			buf = append(buf, ',')
		}

		key, err := json.Marshal(entry.Key)
		if err != nil {
			return nil, err
		}
		val, err := json.Marshal(entry.Value)
		if err != nil {
			return nil, err
		}

		buf = append(buf, key...)
		buf = append(buf, ':')
		buf = append(buf, val...)
	}
	buf = append(buf, '}')
	return buf, nil
}
